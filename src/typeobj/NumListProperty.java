package typeobj;

/**
 * 读取号单的一些特征
 * @author lenovo
 *
 */
public class NumListProperty {
	
	private String header;
	private String encodingin;
	private String encodingout;
	private String firstrow;
	private String columns;
	private String sep;
	private String re;
	private String rep;
	
	public String getHeader() {
		return header;
	}
	public void setHeader(String header) {
		this.header = header;
	}
	public String getEncodingin() {
		return encodingin;
	}
	public void setEncodingin(String encodingin) {
		this.encodingin = encodingin;
	}
	public String getEncodingout() {
		return encodingout;
	}
	public void setEncodingout(String encodingout) {
		this.encodingout = encodingout;
	}
	public String getFirstrow() {
		return firstrow;
	}
	public void setFirstrow(String firstrow) {
		this.firstrow = firstrow;
	}
	public String getColumns() {
		return columns;
	}
	public void setColumns(String columns) {
		this.columns = columns;
	}
	public String getSep() {
		return sep;
	}
	public void setSep(String sep) {
		this.sep = sep;
	}
	public String getRe() {
		return re;
	}
	public void setRe(String re) {
		this.re = re;
	}
	public String getRep() {
		return rep;
	}
	public void setRep(String rep) {
		this.rep = rep;
	}
	
	
}
